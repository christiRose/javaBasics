package class3;

public class ShortHandOperator_4 {
    public static void main(String[] args) {

        //shorter way to do calculation

        int number=10;
        //number=number+10; shortcut number+=10
        number*=20;//whatever value is stored in number variable add 10 to it.
        System.out.println(number);//shorthand operator += shorter way to do mathematics

        int number2=20;
        number2-=10;
        System.out.println(number2);

        int number3=100;
        number3/=10;
        System.out.println(number3);


    }
}
