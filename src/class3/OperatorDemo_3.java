package class3;

public class OperatorDemo_3 {
    public static void main(String[] args) {
       //Modulos operator % "Remainder"

        int number=10;
        System.out.println(number/3); //3
        System.out.println(number%3); //1 remainder
        System.out.println(16%5);//modulos operator to find how many left, to find if its even or odd
        System.out.println(16/5);//if you get 0 means its even number
        System.out.println(1555%5);
        System.out.println(155%2);//we always take the modulos by 2 if the ans is 0 its even number
            //otherwise it's an odd number
    }
}
