        /* Data Types in Java: 1. primitive 2. non primitive
        Primitive: byte, int, short, float, long, double, boolean, char
        numeric:
            whole number: byte, short, int, long
            decimal number: float, double

        non-numeric: boolean, char
   */
        //dataType nameOfTheVariable= value;
        byte num=127;
